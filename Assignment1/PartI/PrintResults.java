
public class PrintResults {
	public static void main(String[] args) {
		int a = 10 + 5;
		int b = 50 - 23;
		int c = 12 * 13;
		double d = 20.0 / 3.0;
		int e = 100 % 7;
		double f = Math.pow(4, 3);
		System.out.printf("The value of a is %d\n", a);
		System.out.printf("The value of b is %d\n", b);
		System.out.printf("The value of c is %d\n", c);
		System.out.printf("The value of d is %f\n", d);
		System.out.printf("The value of e is %d\n", e);
		System.out.printf("The value of f is %f\n", f);
	}
}
